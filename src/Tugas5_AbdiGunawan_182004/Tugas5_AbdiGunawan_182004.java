        double na=0;
        String nh="",ket="",nilaiAkhir;
        
        nt = (int) spnTugas.getValue();
        nm = (int) spnMid.getValue();
        nf = (int) spnFinal.getValue();
        
        na = (0.3 * nt) + (0.3 * nm) + (0.4 * nf);
        
        if (na >= 80) {
            nh = "A";
            ket = "Sangat Baik";
	}
	else if (na >= 70) {
            nh = "B";
            ket = "Baik";
	}
	else if (na >= 51) {
            nh = "C";
            ket = "Cukup";            
	}
	else if (na >= 41) {
            nh = "D";
            ket = "Buruk";            
	}
	else if (na >= 0) {
            nh = "E";
            ket = "Sangat Buruk";
        }

        nilaiAkhir = Double.toString(na);
        txtNa.setText(nilaiAkhir);
        txtNh.setText(nh);
        txtKet.setText(ket);
