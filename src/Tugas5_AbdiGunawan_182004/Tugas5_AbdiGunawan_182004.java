    private void cmbJurusanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbJurusanActionPerformed
        // TODO add your handling code here:
        String Jurusan="";
        if (cmbJurusan.getSelectedItem().equals("TI")) {
            Jurusan = "Teknik Informatika";
        }
        else if (cmbJurusan.getSelectedItem().equals("SI")) {
            Jurusan = "Sistem Informasi";
        }
        else if (cmbJurusan.getSelectedItem().equals("MI")) {
            Jurusan = "Manajemen Informatika";
        }
        else if (cmbJurusan.getSelectedItem().equals("RPL")) {
            Jurusan = "Rekayasa Perangkat Lunak";
        }
        else if (cmbJurusan.getSelectedItem().equals("EN")) {
            Jurusan = "Enterpreneur";
        }
        else if (cmbJurusan.getSelectedItem().equals("DB")) {
            Jurusan = "Digital Bisnis";
        }
        txtJurusan.setText(Jurusan);
        
    }//GEN-LAST:event_cmbJurusanActionPerformed

    private void txtViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtViewActionPerformed
        // TODO add your handling code here:
        String Jkel="",Hobby="";

        //mengambil nilai jenis kelamin dari radio button
        if (rbLaki.isSelected()) {
            Jkel = "Laki-Laki";
        }else if (rbPerempuan.isSelected()) {
            Jkel = "Perempuan";
        }        
        
        //mengambil nilai hobby dari checkbox
        if (cbMancing.isSelected()) {
            Hobby += cbMancing.getText() + ",";
        } 
        if (cbShopping.isSelected()) {
            Hobby += cbShopping.getText() + ",";
        } 
        if (cbCamping.isSelected()) {
            Hobby += cbCamping.getText() + ",";
        }
        if (cbNgoding.isSelected()) {
            Hobby += cbNgoding.getText() + ",";
        }
        if (cbGame.isSelected()) {
            Hobby += cbGame.getText() + ",";
        }
        
        
        
        txtArea.setText(
                "Stambuk                                : " + txtNim.getText() + "\n" +
                "Nama                                     : " + txtNama.getText() + "\n" +
                "Alamat                                    : " + txtAlamat.getText() + "\n" +
                "Tempat & Tanggal Lahir    : " + txtTempatLahir.getText() + "," + cmbTanggal.getSelectedItem() + "-" + cmbBulan.getSelectedItem() + "-" + cmbTahun.getSelectedItem() + "\n" +
                "Jenis Kelamin                      : " + Jkel + "\n" +
                "Hobby                                     : " + Hobby + "\n" +
                "Agama                                    : " + cmbAgama.getSelectedItem() + "\n" +
                "Jurusan                                  : " + txtJurusan.getText() + "\n" +
                "Nilai                                         : " + txtNh.getText() + "\n"           
                
        );
        
    }//GEN-LAST:event_txtViewActionPerformed

